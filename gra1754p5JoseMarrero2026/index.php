<h2>Broccoli Shrimp Alfredo</h2>
		<div id="banner_image"></div>
      <div id="post_text">
		<h3>Ingredients</h3>
		  <p><b>Pasta:</b> Use rotini or fusilli pasta for this dish as pictured here or any other pasta of choice works too.</p>
      <p><b>Shrimp:</b> Use fresh or frozen shrimp and remove the tails. Be careful not to overcook the shrimp so it doesn’t come out rubbery.</p>
      <p><b>Broccoli:</b> Broccoli is a great source of fiber and adds a nice pop of green. But you can also swap it with any other veggie or veggies you’d like.</p>
      <p> <b>Butter:</b> You can use either salted or unsalted butter, just adjust the level of added salt to your taste.</p>
      <p><b>Seasonings:</b> Salt, pepper and garlic – very simple which is standard for alfredo sauces. But I also recommend adding some Italian seasoning and nutmeg if you want to bump it up a notch or find the sauce too simple.</p>
      <p><b>Flour:</b> To thicken the sauce. You can use all-purpose flour or a gluten-free 1:1 flour.</p>
      <p><b>Milk:</b> For a creamy sauce without too many calories and fat from heavy cream.</p>
      <p><b>Cheese:</b> Use a mix of cream cheese and parmesan for a cheesy alfredo sauce.</p>
		<h3>Instructions</h3>
      <p>Bring a large pot of salted water to a boil. Add the pasta and cook until al dente according to package directions. During the last 2 minutes of cooking the pasta, add the broccoli florets to the same pot and allow them to cook with teh pasta. Reserve ½ cup pasta cooking water before draining; set both aside.<br>
<br>
In a large pan over medium heat, heat 1 tablespoon butter. Season the shrimp with salt and pepper and add to the pan. Cook shrimp for 2-3 minutes per side until shrimp turns opaque and is fully cooked; toss with the cooked pasta and broccoli.<br>
<br>

In the same pan, heat the remaining butter, add garlic and cook, stirring constantly, until fragrant, about 1 minute.<br>
<br>

Add flour, then whisk to combine and cook for 1 additional minute. Slowly whisk in the milk, then cream cheese, then let the sauce simmer uncovered until thickened, about 2-3 minutes.<br>
<br>

Add the pasta on top of the sauce and stir well to combine. Add as much reserved pasta water for the desired sauce thickness. Remove from heat and fold in the parmesan cheese.
Serve immediately with more parmesan cheese, if desired.</p>
